package day03_variables;

public class EmployeeInfo {
    public static void main(String[] args) {
        String employeeName = "Emily";
        int age = 30;
        char gender = 'F';
        String companyName = "Google Inc";
        String employeeIdentity = "D11";
        String jobTitle = "Scrum Master";
        int salary = 110000;
        boolean isFullTime = false;
        System.out.println(employeeName);
        System.out.println(age);
        System.out.println(gender);
        System.out.println(companyName);
        System.out.println(employeeIdentity);
        System.out.println(jobTitle);
        System.out.println(salary);
        System.out.println(isFullTime);
        System.out.println("-----------------------------------------------------");
        System.out.println("employeeName = " + employeeName);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("companyName = " + companyName);
        System.out.println("employeeIdentity = " + employeeIdentity);
        System.out.println("jobTitle = " + jobTitle);
        System.out.println("salary = " + salary);
        System.out.println("isFullTime = " + isFullTime);
    }
}
